public class Bit_Operators 
{
    public static void main(String[] args)
    {
        System.out.println("This is AND Operation " + (5&6));//AND Operator
        System.out.println(5|6);//OR operator
        System.out.println(5^6);//XOR
        System.out.println(~5);//NOT
        System.out.println(5<<2);//Left shift operator
        System.out.println(6>>1);//Right Shift Operator
    }
}
